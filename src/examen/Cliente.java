    @Override
    public void run() {
        try {
            ss= new ServerSocket(puerto2);
            String ipd, ipo, user, msj;
            Paquete pack;
            while (true) {                
                so = ss.accept();
                ObjectInputStream in = new ObjectInputStream(so.getInputStream());
                pack = (Paquete) in.readObject();
                ipd = pack.getIpDestino();
                ipo = pack.getIpOrigen();
                user = pack.getUsuario();
                msj = pack.getMensaje();
                so.close();
                
            }
        } catch (Exception e) {
        }
    }
